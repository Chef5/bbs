<script lang="ts" setup>
import { MdEditor, type ToolbarNames } from 'md-editor-v3'
import 'md-editor-v3/lib/style.css'
import { toast } from 'vue-sonner'
import type { z } from 'zod'
import { useColorMode } from '@vueuse/core'
import type { FormSubmitEvent } from '#ui/types'
import { type PostDTO, createPostSchema } from '~/types'

type Schema = z.output<typeof createPostSchema>
const mode = useColorMode()

const route = useRoute()

const toolbars: ToolbarNames[] = [
  0,
  1,
  'bold',
  'underline',
  'strikeThrough',
  'quote',
  'unorderedList',
  'orderedList',
  'codeRow',
  'code',
  'link',
  'image',
  'table',
  'pageFullscreen',
  'preview',
]

const tagRes = await useFetch('/api/go/list', {
  method: 'POST',
  key: 'tagLists',
})
const tags = computed(() => {
  const items = tagRes.data.value?.tags.map((item) => {
    return { name: item.name, id: item.id, desc: `${item.enName} / ${item.desc}` }
  })
  return items
})

const state = reactive<Schema>({
  pid: '',
  title: '',
  content: '',
  tagId: 0,
})

async function loadPost() {
  const query = route.query
  const pid = (query.pid) as string || ''
  if (!pid)
    return
  const res = (await $fetch(`/api/post/${pid}`, {
    method: 'POST',
    body: JSON.stringify({}),
  })) as any as { post: PostDTO }
  Object.assign(state, res.post)
}

await loadPost()
watch(() => route.fullPath, loadPost)

const pending = ref(false)

async function onSubmit(event: FormSubmitEvent<Schema>) {
  pending.value = true
  const result = await $fetch('/api/post/new', {
    method: 'POST',
    body: JSON.stringify(event.data),
  })
  if (result.success && 'pid' in result) {
    userCardChanged.emit()
    setTimeout(() => {
      navigateTo(`/post/${result.pid}`)
    }, 200)
  }
  else if ('message' in result) {
    toast.error(`发表失败,${result.message}`)
  }
  pending.value = false
}

const form = ref<HTMLFormElement>()

async function handleKeyDown(event: KeyboardEvent) {
  if (event.ctrlKey && event.key === 'Enter') {
    form.value?.submit()
  }
}

onMounted(() => {
  window.addEventListener('keydown', handleKeyDown)
})

onUnmounted(() => {
  window.removeEventListener('keydown', handleKeyDown)
})

useHead({
  title: `发表帖子`,
  meta: [
    { name: 'keywords', content: '极简论坛' },
    { name: 'description', content: '极简论坛' },
  ],
})
</script>

<template>
  <UCard class="w-full mt-2">
    <template #header>
      <div class="text-center text-sm">
        发表新帖子
      </div>
    </template>
    <div class="flex flex-col my-2 ">
      <UForm
        ref="form" :schema="createPostSchema" :state="state" :validate-on="['submit']" class="space-y-4"
        autocomplete="off" @submit="onSubmit"
      >
        <UFormGroup label="标题" name="title">
          <UInput v-model="state.title" autocomplete="off" />
        </UFormGroup>
        <UFormGroup label="标签" name="tags">
          <USelectMenu v-model="state.tagId" value-attribute="id" option-attribute="desc" :options="tags" />
        </UFormGroup>
        <UFormGroup label="正文" name="content">
          <client-only>
            <MdEditor
              v-model="state.content" :theme="mode as any" style="max-height: 600px;" :preview="false"
              :toolbars="toolbars" editor-id="newPost" @on-upload-img="onUploadImg"
            >
              <template #defToolbars>
                <XEmoji />
                <XYoutubeDialog />
              </template>
            </MdEditor>
          </client-only>
        </UFormGroup>
        <div>
          <UButton type="submit" :loading="pending">
            发表(Ctrl+Enter提交)
          </UButton>
        </div>
      </UForm>
    </div>
  </UCard>
</template>

